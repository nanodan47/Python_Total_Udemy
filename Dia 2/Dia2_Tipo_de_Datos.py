'''
string(str):    Texto y generalmente está entre comillas (dobes o sencillas) 
                EJEMPLO: 'hola', '%$&', ' ', '123'

integer(int):   Son números y puedes hacer operaciones con ellos
                EJEMPLO: 150, 1, 555, -15, 0

floatin(float): Números decimales, también positivos y negativos y también sirven para operanciones
                EJEMPLO: 1.25, 25.0, 500.50, -95.1

listas(lst):    Colección ordenada de objetos, se escribe entre corchetes y puede contener muchos items en su interior, son ordenadas y cada elemento
                un lugar numerado
                EJEMPLO: [1, 2, 3, 4], ['hola','como ta', 'le ta le tu'], [1, 'hola', 'sal', 'marte',0]
                Posicion  0  1  2  3       0        1           2          0     1      2       3    4 

diccionarios(dic):  Son pares de palabras agrupados que tienen una llave y su valor llave:color valor:rojo. No están ordenados así que se 'podría'
                    invertir, no tienen índice
                    EJEMPLO: {'color':'rojo,'arte':'cine} --> {'arte':'cine, 'color':'rojo}

tuplas(tup):    Son conjunto ordenado de elementos y sus elementos pueden ser de cualquier tipo de datos, es parecido a las listas, pero una tupla
                no se puede ordenar. Es una limitación muy útil
                EJEMPLO: ('lun','mar','mie','jue','vie')

sets(set):      Es un conjunto de elementos ordenado ÚNICO, las listas y tuplas pueden tener elementos repetidos
                EJEMPLO: {'a','b','c','d','e'}

booleanos(bool):    Valores lógicos, solo puede tener dos valores.
                    EJEMPLO: True o False
'''